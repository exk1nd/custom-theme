<p>Many of us have heard about debt management and collections system. But how does it really work? We've covered the topic very briefly already. But now we would like to discuss it a little more.</p>
<h2>Debt Management Plan</h2>
<p>If it's hard for you to make monthly payments on credit bills, a debt management plan can be helpful for you. It's possible to make a single payment to a credit counseling agency. After this, it will do the payments on your behalf.<br />

<p>This plan can't reduce the amount you have to return. However, your credit counselor can make your creditors lower the interest rate or cut down on some fees. Stretching out payments is possible as well. The average duration of a debt management plan is 3-5 years.</p>
<p>A credit counselor will thoroughly research your financial situation and discuss all the available options. You don't have to sign up the very first program it offers you.</p>
<p>For each credit account, included in the plan, you will likely pay enrollment and monthly fees. As a rule, it costs $24 per account.</p>
<p>Secured loans and student loans aren't covered. On the contrary, the unsecured ones are covered. Each month, your counseling agency will get an electronic payment. A progress report will be sent to you each month.</p>
<p>As long as you are in such a program, you have no right to use credit cards. Make timely repayments all the time and meet the creditor's terms.</p>
<h3>What Is Debt Collection?</h3>
<p>Every debt-collection agency specializes in a definite type of borrowing. For instance, an agency can collect loans of more than $200, and not more than 2 years old.</p>
<p>If it is hard for an original creditor to collect a loan, he can sell it to a debt buyer. Such accounts are sold in a group.</p>
<p>The price of debt depends on its type. Buyers keep all the collected debts. They get money for recovering a delinquent account: more recovered debts equals a bigger profit.</p>
<p>Collectors try to convince delinquent borrowers to repay their debts via phone or letters. If they fail, they use software or private investigators. To make the consumers pay, they can also inform the credit bureaus about delinquent payments.</p>
<p>Collectors should count on the debtors to pay. It's not allowed to take a paycheck from a bank account, even if they know the account numbers and the routing. It's possible only after the order of the court.</p>
<p>The collectors have no right to call you before 8 am or after 9 pm. You shouldn't wake up and see the Loan Management and Collections System phone number on your screen.</p>
<p>The reputable collectors neither harass their borrowers nor discriminate them against race, sex, gender or origin. They are fair and law-abiding. If you claim that the loan appeared because of identity theft, they will verify it. They neither report a debt that is older than 7 years nor pursue people who have no money.</p>
