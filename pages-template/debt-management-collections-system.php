<h3>What about Student Loan Debts?</h3>
<p>More and more student loans are going into default. When it happens, the student loan servicer transfers them to a third-party collection company.</p>
<p>If you default on your student loans, talk to a collecting agency assigned to your loan. They will consult you on settling the repayment terms, loan rehabilitation, or even settling the debt.</p>
<p>Debt Management and Collections System U.S. Department of Education aims at helping to repay federal student loans. It can help you repay the existing loan. However, after this, you can get new fees. So ask about all the terms beforehand.</p>
