                    <p>If you want to improve your credit, knowing what you should start with is essential. Your rating mostly depends on the credit scoring model you choose. However, there are some basic aspects that affect your credit score.</p>
                    <p>If you have bad credit and want to get a credit card or a loan, pay attention to the following aspects. These records on your credit record play a crucial role in defining your creditworthiness. </p>

                    <h2>Payment History</h2>
                    <p>Your loan repayment habits impact your credit rating most of all. If you deal with FICO, it will take 35% of your total score, if it is VantageScore – 40%.</p>
                    <p>It means that the easiest way to get bad credit is to miss paydays and ignore your financial obligations. A credit history remembers even the minimum delays in loan or credit card debt repayment. </p>
                    <p>However, this problem is relatively easy to solve. All you need to do is to make on-time payments. You can set an autopay feature or push notifications about the due date to make your life easier.</p>
                    <p>What’s more, many credit providers allow borrowers to postpone their payday. But you can use this feature only once or twice per debt repayment.</p>
                    <p>Note that you may be sent to collections if you don’t fulfill your financial obligations for a long period. If it happens, the collections accounts may greatly damage the borrowers` credit scores.</p>
                    
                    <h2>Credit Utilization</h2>
                    <p>Credit utilization rate is another influential criterion for defining your credit rating. What does it mean, and how is it calculated? Credit utilization shows how much of the available credit you use. The best way to have a high rating is to keep your credit utilization ratio low, no more than 30%.</p>
                    <p>To calculate your ratio check the available line of credit cards. Then add all your revolving credit card balances and divide it by the total credit limit. Lastly, multiply it by 100.</p>
                    <p>If you are unsatisfied with the results, you can improve your credit utilization ratio without any difficulties. To make it possible, try to pay more than your regular installment and ask for a credit limit increase.</p>

