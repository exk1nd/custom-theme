		<p>The following alternatives will serve you in place of a personal loan:</p>
		<ul>
            <li style="font-weight: 400;" aria-level="1">Credit cards: Credit card is a revolving line of credit that offers holders repeated access to borrowed funds. You can use them to pay for anything while deferring the repayment term. It is handy for making quick purchases but rarely has fixed rates.</li>
        </ul>
