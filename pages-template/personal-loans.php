            <li style="font-weight: 400;" aria-level="1">Home Equity Loan: A home equity loan offers a lump sum of money as a second mortgage. You can enjoy a lower interest rate when you take this loan against the equity on your home. The maximum loan amount accounts up to 85% of your home's value.</li>
