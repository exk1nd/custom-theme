		<ul>
            <li style="font-weight: 400;" aria-level="1">Submit identifying documents: The next step to get a personal loan is to complete a formal application and specify some credit information. Usually, this process involves submitting several details, including bank statements, passport, bank account,  individual tax ID number information, etc.</li>
            <li style="font-weight: 400;" aria-level="1">Obtain your personal loan: Once you get approved and sign a loan agreement, the online lender or bank pays the principal loan amount into your savings or checking account you provide. Some lenders may also charge an origination fee.</li>
