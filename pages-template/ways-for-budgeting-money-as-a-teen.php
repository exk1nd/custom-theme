                        <p>Also, every teenager needs to learn the basic rules of financial subtraction. You need to
                            subtract all the necessary expenses from your monthly income. The budget is everything that
                            is
                            left after subtraction.</p>
                        <p>You may use this money to divide it into several categories such as “fun” things or
                            entertainment. No matter how much pocket money is left for you, make sure you spend it
                            wisely
                            and don’t waste more than you’ve planned before.</p>
                        <h3><span style="color: #003300;"><strong>Out of Sight, Out of Mind</strong></span></h3>
                        <p>One of the most effective rules of budgeting for teens is to keep your debit or credit card,
                            not
                            in your wallet but somewhere safe in your car when you go shopping. If you don’t have a car,
                            you
                            may even leave your cards at home and take only cash with you.</p>
                        <p>Make a shopping list in advance and take the average amount of money you will need to spend.
                            Avoid places where you will be tempted to spend a lot of money and later regret it.</p>
                        <p>Don’t go &#8220;window-shopping&#8221; to the malls because most likely you will end up
                            buying
                            something you don’t really need.</p>
