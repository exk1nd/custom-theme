                              rel="noopener">Employment Situation Summary</a> by the U.S. Bureau of Labor Statistics
                        issued in July 2020 has reported that the total unemployment rate fell to 10.2 percent compared
                        to 14.7 percent in April but this is still a scary number. In spite of these figures, there is
                        still a whopping number of people who have lost their jobs or have suffered from a pay cut.</p>
                    <p>Below is the chart from the July <a href="https://www.epi.org/indicators/unemployment/"
