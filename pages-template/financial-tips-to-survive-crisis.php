                    <h3><img alt="unemployment rate" width="960" height="763" data-srcset="<?= get_template_directory_uri() . '/assets/img/unemployment.png'?>"><br/>
                        Top Advice to Manage Your Finances During Crisis</h3>
                    <h3><span style="color: #084c8d;">#1 Save More</span></h3>
                    <p>Coronavirus has not only caused people to be scared for their health but also led to serious
                        economic consequences that are not finished yet. Millions of people have lost their positions
                        and don’t know how to make ends meet. Others faced pay cuts that led to less spending and more
                        saving.</p>
                    <p>People finally realized that having an emergency fund is a necessity these days.</p>
                    <p
                            style="border-left: 7px solid #084c8d; ; text-align: left; padding-left: 18px; line-height: 1.5; font-size: 16px; font-style: italic;">
                        &#8220;One of the greatest tips for consumers is to use the savings from no traveling, eating
                        out, etc. to build up your emergency fund. Also, formulate your budget to see if there are any
                        adjustments you can make. Refinance your mortgage (if you have one) since rates are at their
                        lowest level ever and this will improve your cash flow,&#8221; says <a
                                href="https://www.plannersearch.org/financial-advisor/thomas-w-balcom" target="_blank"
                                rel="noopener">Thomas W. Balcom</a>.</p>
                    <p>The economic recession of 2008 was long gone and people thought it won’t come back so soon.
                        However, here we are, with businesses having no consumers. If there are no consumers who spend
                        cash, the economy is not growing. Hence, one of the most essential tips from financial experts
                        these days is to start saving as much as you can.</p>
                    <p>Nobody can predict how long it will take to recover from this crisis, so saving funds should be
                        your top priority.</p>
                    <p>Since the beginning of the lockdown, several industries have been at a full standstill, such as
                        travel, service, and retail. Consumers don’t go out, they don’t travel, and they spend cash only
                        on the essentials including rent, mortgage, car, and groceries.</p>
                    <p>So, it’s a perfect time for you to start saving funds for at least three to six months’ worth of
                        expenditures. This is the minimum amount you may need to remain financially afloat during
                        challenging times or in between jobs.</p>
                    <h3><span style="color: #084c8d;">#2 Invest</span></h3>
                    <p>The second step is about investing. While saving is much safer and presents no risks for you,
                        investing can be a great opportunity to obtain extra funds for the future. As you may already
                        know, having a steady income source today doesn’t mean steady employment tomorrow.</p>
                    <ul>
                        <li><em><strong>Everything might change all of a sudden, and this is the biggest lesson COVID-19
                                    has taught us.</strong></em></li>
                    </ul>
                    <p>It pays to think about the future and opt for alternative ways to gain profit. For instance,
                        peer-to-peer investing can be beneficial. This is when borrowers are connected with investors
                        directly through web platforms. Of course, you face certain risks and might even lose your funds
                        but it can be a useful method to diversify your income and search for new ways of financial
                        well-being.</p>
                    <h3><span style="color: #084c8d;">#3 Spend Less</span></h3>
                    <p>This is obvious that we should cut our spending during the crisis. It is helpful for consumers
                        but destructive for the economy of the country. If people stop spending cash, it leads to the
                        contraction of the economy. As a result, businesses collapse, people are laid off, and crisis
                        worsens.</p>
                    <p
                            style="border-left: 7px solid #084c8d; ; text-align: left; padding-left: 18px; line-height: 1.5; font-size: 16px; font-style: italic;">
                        &#8220;Knowing where your paycheck is going is critical to developing a sound financial plan and
                        making money moves in any environment. The first step is to determine your essential expenses.
                        How much are your essential expenses today? Generally, a good rule to follow is to keep your
                        monthly housing expense (rent or mortgage payment) less than 28% of your gross income and
                        keeping your total debt payments (housing, car, credit cards) less than 36% of your gross
                        income,&#8221; mentions <a href="https://www.linkedin.com/in/josephrstemmle/" target="_blank"
                                                   rel="noopener">Joseph R. Stemmle</a>, &#8220;Most families should set
                        aside enough emergency
                        cash to cover 3-6 months of their essential expenses. Essentials include housing, utilities,
                        groceries, medical expenses, etc.&#8221;</p>
                    <p>So, it is important to spend money as well. But you need to learn how to do it right. You should
                        boost your >budgeting skills and know how much exactly you need to allocate to each spending
                        category. Of course, <a href="/what-are-emergency-loans-for-rent/">paying for rent</a> or
                        mortgage, utilities, auto, and groceries are your essentials that remain stable from month to
                        month.</p>
                    <p>But if we all want to revive and support our economy, we should take action. You can research for
                        special offers, discounts, and bonuses from various industries. Calculate how much you can spend
                        and choose affordable deals.</p>
                    <h3><span style="color: #084c8d;">#4 Borrow Wisely</span></h3>
                    <p>Many consumers have credit cards or mortgages to pay off. Thousands of people rely on some
                        lending options in their daily lives.</p>
                    <p>The current pandemic is no exception. People search for suitable borrowing options to make ends
                        meet or consolidate existing debts.</p>
                    <p>The temporary relief in the form of payment holidays is tempting to accept. Yet, you should be
                        careful with your debt – the interest will still increase even during such
                        &#8220;holidays&#8221; and it will be your responsibility to pay more once they are finished as
                        your debt will still remain.</p>
                    <p>Some people suggest that they can skip payments during these months as this data won’t be
                        reported to their credit profiles.</p>
                    <p>It is true but skipping monthly payments may affect your ability to <a
                                href="/">apply for loans</a> in the future. Creditors will want to
                        conduct a credit pull to verify you will be able to repay the new debt while still having a
                        previous one.</p>
                    <p>Hence, you should consider this opportunity only as your last resort. Accept payment holidays
                        only if you really have no income at all. In case you have steady employment and receive a
                        regular income, it’s better not to accumulate debt but rather pay it off on time.</p>
                    <h3>Coronavirus-related Financial Assistance for People</h3>
                    <p>Take advantage of the following relief measures during a pandemic:</p>
                    <ul>
                        <li><em><strong>Get qualified for paid leave</strong></em></li>
                    </ul>
                    <p>The Families First Coronavirus Response Act allows people to get two weeks of paid leave. It is a
                        great opportunity to stay safe at home when you have to telecommute to work but need to remain
                        with your family during the lockdown. If you are under a quarantine order or need to take care
                        of small children who are out of school or daycare, this is a wonderful chance to get paid while
                        being at home.</p>
                    <ul>
                        <li><em><strong>Assistance with Utility Bills</strong></em></li>
                    </ul>
                    <p>Some utility companies and service providers offer special payment plans during the pandemic.
                        Also, you may get certain charges waived, especially if you had late payments. Housing and Urban
                        Development’s COVID-19 resources can be helpful for those who seek extra assistance with their
                        utility bills.</p>
                    <ul>
                        <li><em><strong>Rent/Mortgage Relief</strong></em></li>
                    </ul>
                    <p>People who rent or have mortgage payments they struggle paying off can turn to the CARES Act and
                        receive additional assistance. For example, consumers can benefit from a 90-day eviction ban set
                        in New York and other cities.</p>
                    <ul>
                        <li><em><strong>Postponing Taxes</strong></em></li>
                    </ul>
                    <p>The deadline for filing taxes can be postponed up till September as of today. This way you are
                        allowed to conduct all the necessary paperwork until this time and have extra time to <a
                                href="/7-funny-money-saving-tips/">save enough money</a> for paying your taxes. You
                        can
                        check the states that delayed tax filing and payments.</p>
                    <p>All in all, follow these pieces of advice and practical tips to maintain financial health and
                        improve your monetary well-being during these challenging times.</p>

