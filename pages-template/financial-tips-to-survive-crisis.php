                    <h2>Current Economic Situation and Consequences for Personal Finances</h2>
                    <p>The number of citizens who applied for unemployment benefits in March was over 10 million, which
                        was a new record. Millions of Americans were furloughed or laid off due to the pandemic. The
                        efforts taken by the government to improve the situation led to some positive effects in the
                        labor market.</p>
