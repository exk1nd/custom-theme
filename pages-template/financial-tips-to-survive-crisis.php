                            style="border-left: 7px solid #5cbb16; text-align: left; padding-left: 18px; line-height: 1.8; font-size: 16px; font-style: italic;">
                        &#8220;Nobody likes to necessarily plan for the unexpected because it can be unpleasant,
                        intimidating, overwhelming, and frightening. I think financial planning helps folks feel more
