                        cuts.</p>
                    <p><span id="more-13623"></span></p>
                    <p>It is scary and devastating to live in the new world with no stability at all. Many employers who
                        paid their employees for the first months of lockdown have become reluctant to continue the
                        payments.</p>
                    <p>It is understandable as businesses don’t have profits as consumers don’t <a
