                        <p>International travel is an increasingly popular choice for students and young people. In
                            2012, youth travelers spent $217 billion on tourism spend worldwide.<span
                                    id="more-14154"></span></p>
                        <p>Fortunately, it’s getting easier and cheaper to see the world, and you can save big when
                            traveling by making some smart moves with your money.</p>
                        <h2>Voluntourism</h2>
                        <p>First, think about what kind of overseas adventure you want. There are many ways to go about
                            it, and volunteer tourism, or what’s come to be known as “voluntourism” is a fast-growing
                            trend. <a
                                    href="https://www.responsibletravel.org/wp-content/uploads/sites/213/2021/03/trends-and-statistics-2019.pdf"
                                    target="_blank" rel="noopener">According to the Marriott Rewards Credit Card from
                                Chase survey</a>, 84% of millennials, 68% of Gen X-ers, and
                            51% of Baby Boomers said that they would travel abroad to participate in volunteer
                            activities.</p>
                        <p>The idea is that volunteers experience a country and culture by working on a local community
                            project (usually in a developing country), and the community benefits from the work of the
                            volunteers. Common examples are working in an orphanage, helping to build a school, and
                            doing conservation work.</p>
                        <p>Volunteers usually pay a (somewhat) inclusive fee to placement providers. Programs start from
                            about $1,700 for a week-long placement in Nepal, for example, and go all the way up to
                            $29,995 for a 27-week, multi-country trip.</p>
                        <h3>Gap Year</h3>
                        <p>Another option is a gap year, a period ranging from three months to two years in which you
                            usually live and work overseas. About 30,000-40,000 American students choose to take a gap
                            year every year, and the numbers continue to grow. The <a
                                    href="http://www.gapyearassociation.org/wp-content/uploads/2021/05/2020-GYA-Survey-Report.pdf"
                                    target="_blank" rel="noopener">American Gap Association reported</a> that the
                            average gap year is usually between 6 to 12 months and costs between $5,000 and $10,000.</p>
                        <h3>But what about managing your money?</h3>
                        <p>No matter how you go about your travels, one thing you’ll have to consider is how you’ll
                            manage your money. Assuming you’ll be working, you’ll probably need to open a bank account
                            in your chosen country to get your wages paid into, and you’ll want a debit card to make
                            everyday purchases easier.</p>
                        <p>It will usually be much more cost-effective than converting your foreign money back to your
                            American account and converting back to foreign money when withdrawing it. There are even
                            ways to travel without using your credit card at all.</p>
                        <h3>Money Transfers</h3>
                        <p>Transferring money and converting currencies can be costly – something you want to avoid when
                            backpacking overseas. If you’ve got savings in an American bank account and want to transfer
                            that money into your foreign bank account, or need to transfer costs to a business in a
                            foreign country, you’ve got a couple of options.</p>
                        <p>Let’s say you’ve saved $10,000 to get you started in sunny Sydney, Australia.</p>
                        <p>You’ve set up an Australian bank account, and now you need to transfer your funds from your
                            American bank account.</p>
                        <p>The “old school” way of doing this is a simple bank to bank transfer.</p>
                        <p>Your bank will have an exchange rate for Australian dollars – let’s say 1 USD = 1.28 AUD.</p>
                        <p>You’ll be charged a transaction fee (about $35), and in some cases, you might also be charged
                            a receiving fee and other nasties. But let’s give your bank the benefit of the doubt.</p>
                        <p>With a 1.28 exchange rate and a $35 transaction fee, you’re $10,000 USD will get you $12,765
                            AUD.</p>
                        <p><b>You can probably do much better than that.</b></p>
                        <h3>Go Online</h3>
